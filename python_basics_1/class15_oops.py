class Venkat:
    def __init__(self,a,b):
        self.a=a
        self.b=b       
    def kiran(self):
        print(self.a,self.b) 
obj=Venkat(2,3)#creating object
print(obj.a)#accessing the class variable using object
obj.kiran()
